package interviewtasks;

public class SwappingValues {

	public static void main(String[] args) {
	
		int n1= 10;
		int n2= 20;
		
		int temp=0;
		
		System.out.println("before swapping n1:="+n1+" n2:="+n2);
		
		temp=n1;
		n1=n2;
		n2=temp;
		
		System.out.println("after swapping  n1:="+n1+" n2:="+n2);
		
		

	}

}
